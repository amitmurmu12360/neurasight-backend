/**
 * Detect Industry using Sovereign Industry Library
 * 
 * Uses Symbolic Feature Vector matching for deterministic detection.
 * Falls back to header-based detection if feature vector is unavailable.
 */
function detectIndustry(data: DashboardState): IndustryType {
  // Check for Retail indicators: "Sales" and "Profit" columns (from Agent 1's mapping)
  // If data has been mapped from Retail dataset, check for Retail-specific patterns
  const context = data as any;
  
  // Check if we have Retail-specific data patterns
  // Retail datasets typically have "Sales" mapped to ARR and "Profit" mapped to NRR
  // We can detect this by checking if the financials structure suggests Retail
  if (context._sourceType === "retail" || context._detectedDomain === "retail") {
    return "retail";
  }
  
  // SYMBOLIC FEATURE VECTOR: Try to calculate feature vector from EDA insights
  const edaInsights = context._eda_insights as {
    descriptive_stats?: {
      arr?: { std_dev?: number; mean?: number };
      sales?: { std_dev?: number; mean?: number };
    };
  } | undefined;
  
  if (edaInsights?.descriptive_stats) {
    // Extract variance from EDA insights
    const arrStdDev = edaInsights.descriptive_stats.arr?.std_dev || 
                      edaInsights.descriptive_stats.sales?.std_dev || 0;
    const arrMean = edaInsights.descriptive_stats.arr?.mean || 
                    edaInsights.descriptive_stats.sales?.mean || 0;
    
    // Calculate variance (std_dev^2)
    const variance = Math.pow(arrStdDev, 2);
    
    // Estimate time delta and sparsity from data structure
    // For now, use variance-based detection
    const featureVector: { variance: number; timeDelta: number; sparsity: number } = {
      variance,
      timeDelta: 30, // Default to monthly (will be refined with actual timestamp data)
      sparsity: variance > 1000 ? 0.7 : 0.3, // High variance = high sparsity (transactional)
    };
    
    // Match feature vector to industry
    const matchedIndustry = matchFeatureVectorToIndustry(featureVector);
    if (matchedIndustry !== "generic") {
      return matchedIndustry;
    }
  }
  
  // Fallback: Check for explicit Retail indicators in the data structure
  // If we have Sales/Profit patterns instead of ARR/NRR, it's Retail
  if (data.financials?.arr && data.financials?.nrr) {
    // Check if the values suggest Retail (e.g., very high ARR might be total Sales)
    // For now, if we have ARR and NRR, assume SaaS
    return "saas";
  }
  
  // Default to SaaS for backward compatibility
  return "saas";
}

